    private void getRevText() {
        reviewBlocks = "";
        for (Review r : reviews) {
            reviewBlocks += assembleReview(r.establishment(), r.address(), r.notes(), r.rating()) + "\n";
        }
        
        if (!reviews.isEmpty()) {
            ReviewPane.setText(reviewBlocks);
        }
    }
    
    private void setupFiles() {
        dir = new File("notes");        
        try {
            dir.mkdirs();
        }
        catch (Exception ex) {
            System.out.println("Error creating file directory");
        }   
        
        database = new File(dir, "reviews.txt");
        try {
            database.createNewFile();
        }
        catch (IOException ex) {
            System.out.println("Error creating database file");
        }
        
        reviewRW = new FileIO(database.getPath());
    }
    
