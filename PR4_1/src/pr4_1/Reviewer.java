     private String assembleReview(String name, String address, String notes, int rating) {
         String review = "Restaurant: " + name + "\n";
         review += "Location: " + address + "\n";
         review += "Rating: " + rating + "/5\n";
         review += "Additional notes: " + notes + "\n";
         
         return review;
    }
    
